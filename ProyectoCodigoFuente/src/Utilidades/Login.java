    /**
     * metodo que servira para validar un usuario en el sistema
     */
    private void Validar(){
        String nombreUsuario= tfUsuario.getText();
        String pass = new String(tfPass.getPassword());
        /**JPA maneja todas las operaciones CRUD(Create, Read, Update, Delete - Crear, Leer, Actualizar, Borrar)
         * a trav√©s de la interface EntityManager.
         */
        if(!nombreUsuario.equals("")){
            /** Se busca el usuario con el nombre de usuario del campo usuario a travez del metodo find del entitymanager
             */
            consultaEmpleado.setParameter("usuario", nombreUsuario);
            //setEmpleado(setUsuario(entityManager1.find(Usuarios.class, nombreUsuario)));
             /** Si se encuantra el usuario se accedera al menu principal, en caso contrario se saldra del sistema*/

            try{
                empleado = (Empleados) consultaEmpleado.getSingleResult();
            }catch(javax.persistence.NoResultException e){
                System.out.println("prueba");
            }
            
             if(empleado!=null){
                 if(empleado.getUsuario().getPassword().equals(pass)){
                     this.setVisible(false);
                 }else{
                     JOptionPane.showMessageDialog(this, "El usuario o password es incorrecto", "Atencion", (int) CENTER_ALIGNMENT);
                     tfPass.setText("");

                 }
             }else{
                 JOptionPane.showMessageDialog(this, "El usuario o password es incorrecto", "Atencion", WIDTH);
                 tfPass.setText("");
             }
        }
    }

