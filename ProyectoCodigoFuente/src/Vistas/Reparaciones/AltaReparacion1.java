    /**
     * Metodo para dar de alta una reparacion
     */
    private void alta(){

        reparacion.setObservaciones(taObservaciones.getText());
        reparacion.setDiagnostico(taDiagnostico.getText());
        reparacion.setEstado((String)cbEstados.getSelectedItem());
        reparacion.setIdCliente(listaClientes.get(cbClientes.getSelectedIndex()));
        reparacion.setIdEmpleado((Empleados)cbEmpleados.getSelectedItem());
        reparacion.setAccionesList(new ArrayList<Acciones>());
        setAcaptaAlta(true);
        this.dispose();

    }

    /**
     * Se encargara de cerrar la ventana
     */
    private void Cerrar(){
        int opcion= JOptionPane.showConfirmDialog(this, "Desea cancelar el alta", "Atencion", JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);

        if(opcion==JOptionPane.YES_OPTION){
           acaptaAlta=false;
           this.dispose();
        }
    }

